public class VideoFunData {
    private String mDirector = "";//导演
    private String mEditor = "";//编剧
    private String mActors = "";//主演
    private String mType = "";//类型
    private String mArea = "";//地区
    private String mLanguage = "";//语言
    private String mTime = "";//上映时间
    private String mLength = "";//片场
    private String mAnotherName = "";//别名
    private String mRank = "";//评分

    public String getDirector() {
        return mDirector;
    }

    public VideoFunData setDirector(String director) {
        mDirector = director;
        return this;
    }

    public String getEditor() {
        return mEditor;
    }

    public VideoFunData setEditor(String editor) {
        mEditor = editor;
        return this;
    }

    public String getActors() {
        return mActors;
    }

    public VideoFunData setActors(String actors) {
        mActors = actors;
        return this;
    }

    public String getType() {
        return mType;
    }

    public VideoFunData setType(String type) {
        mType = type;
        return this;
    }

    public String getArea() {
        return mArea;
    }

    public VideoFunData setArea(String area) {
        mArea = area;
        return this;
    }

    public String getLanguage() {
        return mLanguage;
    }

    public VideoFunData setLanguage(String language) {
        mLanguage = language;
        return this;
    }

    public String getTime() {
        return mTime;
    }

    public VideoFunData setTime(String time) {
        mTime = time;
        return this;
    }

    public String getLength() {
        return mLength;
    }

    public VideoFunData setLength(String length) {
        mLength = length;
        return this;
    }

    public String getAnotherName() {
        return mAnotherName;
    }

    public VideoFunData setAnotherName(String anotherName) {
        mAnotherName = anotherName;
        return this;
    }

    public String getRank() {
        return mRank;
    }

    public VideoFunData setRank(String rank) {
        mRank = rank;
        return this;
    }
}
